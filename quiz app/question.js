function getNewQn() {
  question.innerHTML = all[currentQuestion].question;
  choices.forEach(function (e) {
    e.innerHTML = `<span></span>${
      all[currentQuestion][`choice${e.getAttribute("data-number")}`]
    }`;
  });
  currentQuestion++;
}

confirmBtn.addEventListener("click", function (e) {
  let answer;
  radio.forEach(function (e) {
    if (e.checked === true) {
      answer = e.getAttribute("data-number");
    }
  });
  if (all[currentQuestion - 1].correct === answer) {
    alertS.classList.add("suc");
    alertS.textContent = "صح يا إبني ربنا يحميك";
    score += 2;
  } else {
    alertS.classList.add("danger");
    alertS.textContent = "غلط يا أستاذ";
  }
  confirmBtn.style.pointerEvents = "none";
  nextBtn.style.display = "unset";
});

nextBtn.addEventListener("click", function (e) {
  confirmBtn.style.pointerEvents = "unset";
  nextBtn.style.display = "none";
  alertS.className = "alert";
  scoreText.textContent = `عدد النقاط:${score}`;
  qNumber.textContent = `السؤال رقم:${currentQuestion}`;
  localStorage.setItem("score", score);
  if (all.length <= currentQuestion) {
    location.assign("./end.html");
  } else {
    getNewQn();
  }
});
